package com.herongtech.console.domain.common.bean;

import com.herongtech.console.core.common.search.BaseSearchBean;

public class TaskSearchBean extends BaseSearchBean {

	
	private String minWorkday;
	private String maxWorkday;
	private String taskName;
	private String dealStatus;
	public String getMinWorkday() {
		return minWorkday;
	}
	public void setMinWorkday(String minWorkday) {
		this.minWorkday = minWorkday;
	}
	public String getMaxWorkday() {
		return maxWorkday;
	}
	public void setMaxWorkday(String maxWorkday) {
		this.maxWorkday = maxWorkday;
	}
	public String getTaskName() {
		return taskName;
	}
	public void setTaskName(String taskName) {
		this.taskName = taskName;
	}
	public String getDealStatus() {
		return dealStatus;
	}
	public void setDealStatus(String dealStatus) {
		this.dealStatus = dealStatus;
	}
	
}
