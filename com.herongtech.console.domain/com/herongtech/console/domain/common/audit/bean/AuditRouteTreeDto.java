package com.herongtech.console.domain.common.audit.bean;

import java.io.Serializable;



public class AuditRouteTreeDto implements Serializable {
	private static final long serialVersionUID = 1L;
	
	/**节点id*/
	private String id;
	
	/**节点名称*/
	private String name;
	
	/**父节点id*/
	private String parent;
	
	/**树节点类型*/
	private String treeNodeType;
	
	/**节点机构级别*/
	private String anBrchLvl;


	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}


	public String getTreeNodeType() {
		return treeNodeType;
	}

	public void setTreeNodeType(String treeNodeType) {
		this.treeNodeType = treeNodeType;
	}

	public String getAnBrchLvl() {
		return anBrchLvl;
	}

	public void setAnBrchLvl(String anBrchLvl) {
		this.anBrchLvl = anBrchLvl;
	}

    
    public String getId() {
        return id;
    }

    
    public void setId(String id) {
        this.id = id;
    }

    
    public String getParent() {
        return parent;
    }

    
    public void setParent(String parent) {
        this.parent = parent;
    }

	
	
}
