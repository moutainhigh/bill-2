package com.herongtech.console.service.callremote.bean;


public class Item {
    private String exfield;
    private String field;
    private String fieldName;
    private String valueType;
    private String defaultvalue;
    private String javabeanAlias;
    private String required;
    
    
    
    
    public String getExfield() {
        return exfield;
    }

    
    public void setExfield(String exfield) {
        this.exfield = exfield;
    }

    public String getField() {
        return field;
    }
    
    public void setField(String field) {
        this.field = field;
    }
    
    public String getFieldName() {
        return fieldName;
    }
    
    public void setFieldName(String fieldName) {
        this.fieldName = fieldName;
    }
    
    public String getValueType() {
        return valueType;
    }
    
    public void setValueType(String valueType) {
        this.valueType = valueType;
    }
    
    public String getDefaultvalue() {
        return defaultvalue;
    }
    
    public void setDefaultvalue(String defaultvalue) {
        this.defaultvalue = defaultvalue;
    }
    
    public String getJavabeanAlias() {
        return javabeanAlias;
    }
    
    public void setJavabeanAlias(String javabeanAlias) {
        this.javabeanAlias = javabeanAlias;
    }
    
    public String getRequired() {
        return required;
    }
    
    public void setRequired(String required) {
        this.required = required;
    }
    

}
