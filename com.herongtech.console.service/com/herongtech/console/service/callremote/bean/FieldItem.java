package com.herongtech.console.service.callremote.bean;

import java.util.List;


public class FieldItem {
    
    private String transNo;
    private String transName;
    private String rootElement;
    private String headElement;
    private String bodyElement;
    private List<Item> head;
    private List<Item> body;

    
    
    
    
    public String getTransNo() {
        return transNo;
    }


    
    public void setTransNo(String transNo) {
        this.transNo = transNo;
    }


    
    
    public String getTransName() {
        return transName;
    }



    
    public void setTransName(String transName) {
        this.transName = transName;
    }



    public String getRootElement() {
        return rootElement;
    }


    
    public void setRootElement(String rootElement) {
        this.rootElement = rootElement;
    }


    
    public String getHeadElement() {
        return headElement;
    }


    
    public void setHeadElement(String headElement) {
        this.headElement = headElement;
    }


    
    public String getBodyElement() {
        return bodyElement;
    }


    
    public void setBodyElement(String bodyElement) {
        this.bodyElement = bodyElement;
    }


    public List<Item> getHead() {
        return head;
    }

    
    public void setHead(List<Item> head) {
        this.head = head;
    }

    
    public List<Item> getBody() {
        return body;
    }

    
    public void setBody(List<Item> body) {
        this.body = body;
    }
    
    

}
